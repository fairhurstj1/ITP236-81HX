                - insert the row during the migration
                - update the row if the seed changes
                - delete the row if the seed is removed
              * EF Core cannot rely on auto‑generated Identity values for this, because Identity values are:
                - generated by SQL Server
                - not known ahead of time
                - not guaranteed to be the same on every machine
            */
