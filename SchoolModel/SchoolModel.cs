    [Table("Student")]
    public class Student
    {
        public int StudentId { get; set; }

        public required string FirstName { get; set; }
        public required string LastName { get; set; }

        public string? Major { get; set; }
#if Part2
        /// <summary>
        /// Gets or sets the email address. This is added AFTER the initial build
        /// </summary>
        public string? Email { get; set; }

        [MaxLength(15)]
        /// <summary>
        /// Gets or sets the phone number associated with the entity. This too is added AFTER the initial build.
        /// Migration looks like this afterwards:
        ///  migrationBuilder.AddColumn<string>(
        ///  name: "PhoneNumber",
        ///        schema: "School",
        ///        table: "Student",
        ///        type: "nvarchar(15)",
        ///        nullable: true,
        ///        defaultValue: "");
        /// </summary>
        public string? PhoneNumber { get; set; }
#endif
        /// <summary>
        /// Gets or sets the collection of enrollments associated with the Student.
        /// A Student can have Many Enrollments--Can be enrolled in many courses
        /// </summary>
        public List<Enrollment> Enrollments { get; set; } = new();
#if Part4
        /// <summary>
        /// Gets or sets the collection of clubs that a Student belongs to.  Added new AFTER the initial build.
        /// </summary>
        public List<Club> Clubs { get; set; } = new();
#endif
#if Part5
        [NotMapped]             //--< Does not store this in the DB <<<
        public double? GPA =>
            Enrollments
                .Where(e => e.GradePoints.HasValue)
                .Select(e => e.GradePoints!.Value)
                .DefaultIfEmpty()
                .Average();
#endif
#if Part6
        [NotMapped]             //--< Does not store this in the DB <<<
        public double? GPA
        {
            get
            {
                var gradePoints = Enrollments
                    .Where(e => e.GradePoints.HasValue)
                    .Select(e => e.GradePoints!.Value)
                    .ToList();

                return gradePoints.Count == 0
                    ? null
                    : gradePoints.Average();
            }
        }
#endif
#if Part7
[NotMapped]
public double? GPA
{
    get
    {
        var graded = Enrollments
            .Where(e => e.GradePoints.HasValue && e.Course != null)
            .Select(e => new
            {
                Points = e.GradePoints!.Value,
                Credits = e.Course.Credits
            })
            .ToList();

        if (graded.Count == 0)
            return null;

        double totalPoints = graded.Sum(g => g.Points * g.Credits);
        double totalCredits = graded.Sum(g => g.Credits);

        return totalPoints / totalCredits;
    }
}
        [NotMapped]
        public int EnrollmentCount => Enrollments.Count;
#endif
        /// <summary>
        /// Uses LINQ to get the full name, consisting of the first name followed by the last name.
        /// </summary>
        public string FullName => $"{FirstName} {LastName}";
    }

    [Table("Course")]
    public class Course
    {
        public int CourseId { get; set; }

        public required string Tag { get; set; }
        public required string Title { get; set; }
#if Part3
        /// <summary>
        /// Gets or sets the unique identifier of the teacher. Added AFTER the initial build. This is a new 1:M relationship
        /// </summary>
        public int? TeacherId { get; set; }
        /// <summary>
        /// Teacher for the course. Added AFTER the initial build
        /// </summary>
        public Teacher? Teacher { get; set; }
#endif

        public int Credits { get; set; }
        /// <summary>
        /// Gets or sets the collection of enrollments associated with the Course.
        /// A Course can have Many Enrollments--Can have many Students enrolled in the course.
        /// </summary>
        public List<Enrollment> Enrollments { get; set; } = new();
        [NotMapped]
        public int EnrollmentCount => Enrollments.Count;
    }
    /// <summary>
    /// Represents one student's enrollment in one course, including grade and related entities.
    /// </summary>
    /// <remarks>Includes navigation properties to the associated student and course.</remarks>
    [Table("Enrollment")]
    public class Enrollment
    {
        public int EnrollmentId { get; set; }

        public int StudentId { get; set; }
        public Student Student { get; set; } = null!;


        public int CourseId { get; set; }
        public Course Course { get; set; } = null!;

#if Part3
        public string? Grade { get; set; }

        public double? GradePoints =>
            Grade switch
            {
                "A" => 4.0,
                "B" => 3.0,
                "C" => 2.0,
                "D" => 1.0,
                "F" => 0.0,
                _ => null
            };
#endif
    }
#if Part3
